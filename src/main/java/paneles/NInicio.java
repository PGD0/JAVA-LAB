    private void AGREGARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AGREGARActionPerformed
        // TODO add your handling code here:
        String nombre = nombretxt.getText();
        String direccion = directxt.getText();

        try {
            if(nombre.equals("") || direccion.equals("")){
                JOptionPane.showMessageDialog(null, "Faltan por ingresar datos");
            } else {
                String sql = "INSERT INTO `sucursales`( `Nom_Sucursal`, `Direccion`) VALUES ('"+nombre+"','"+direccion+"')";
                PreparedStatement ps = cn.prepareCall(sql);
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Se guardaron correctamente los datos");
                LimpiarTabla();
                consulta();
                totalPiezas();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_AGREGARActionPerformed

    private void ELIMINARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ELIMINARActionPerformed
        // TODO add your handling code here:
        int fila = Tabla.getSelectedRow();
        try {
            if (fila<0) {
                JOptionPane.showMessageDialog(null, "Cliente no seleccionado");
                LimpiarTabla();
                consulta();
                totalPiezas();
            } else {
                String sqlProductos = "DELETE FROM productos WHERE Cod_Sucursal='" + idc + "'";
                String sqlSucursales = "DELETE FROM sucursales WHERE ID_Sucursal='" + idc + "'";
                PreparedStatement psProductos = cn.prepareCall(sqlProductos);
                PreparedStatement psSucursales = cn.prepareCall(sqlSucursales);
                psProductos.executeUpdate();
                psSucursales.executeUpdate();
                JOptionPane.showMessageDialog(null, "Dato borrado correctamente");
                LimpiarTabla();
                consulta();
                totalPiezas();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_ELIMINARActionPerformed

    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // TODO add your handling code here
        String codPieza = codPiezatxt.getText();
        int cantidad = 0;
        String name = "";
        try {
            String sql = "SELECT * FROM productos WHERE Cod_Pieza='"+codPieza+"'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
 
            while (rs.next()) {
                name = rs.getString("Nombre");
                cantidad += rs.getInt("Stock");
            }
            JOptionPane.showMessageDialog(null, "Producto: "+name+"\nStock: "+cantidad);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_BuscarActionPerformed

    private void BUSCAR_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BUSCAR_ActionPerformed
        // TODO add your handling code here:
        int cedula = Integer.parseInt(cedulatxt.getText());
        String doc_iden = "";
        String nombres = "";
        String apellidos = "";
        int suclab = 0;
        int tipo = 0;
        String rol = "";
        try {
            String sql = "SELECT * FROM usuarios WHERE Doc_ident='"+cedula+"'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
 
            while (rs.next()) {
                doc_iden = rs.getString("Doc_ident");
                nombres = rs.getString("Nombres");
                apellidos = rs.getString("Apellidos");
                suclab = rs.getInt("Suc_Lab");
                tipo = rs.getInt("ID_Rol");
            }
            if(tipo==1){
                rol = "Administrador";
            } else {
                rol = "Vendedor";
            }
            JOptionPane.showMessageDialog(null, "Documento: "+doc_iden+"\nNombres: "+nombres+"\nApellidos: "+apellidos+"\nID Sucursal donde labura: "+suclab+"\nRol: "+rol);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }//GEN-LAST:event_BUSCAR_ActionPerformed

