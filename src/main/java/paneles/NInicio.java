    void LimpiarTabla() {
        for(int i = 0; i < Tabla.getRowCount(); i++){
            modelo.removeRow(i);
            i = i-1;
        }
        for(int j = 0; j < Tabla2.getRowCount(); j++){
            modelo2.removeRow(j);
            j = j -1;
        }
        Nuevo();
    }
    
    void totalPiezas(){
        try {
            String sql = "SELECT Cod_Sucursal, SUM(Stock) AS Total_Piezas FROM productos GROUP BY Cod_Sucursal";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            Object[] cli = new Object[2];
            modelo2 = (DefaultTableModel) Tabla2.getModel();
            while (rs.next()) {
                cli [0] = rs.getInt("Cod_Sucursal");
                cli [1] = rs.getString("Total_Piezas");
                
                modelo2.addRow(cli);
            }
            
            Tabla2.setModel(modelo2);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }
    
    void consulta() {
        try {
            String sql = "SELECT * FROM sucursales";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            Object[] cliente = new Object[3];
            modelo = (DefaultTableModel) Tabla.getModel();
            while (rs.next()) {
                cliente [0] = rs.getInt("ID_Sucursal");
                cliente [1] = rs.getString("Nom_Sucursal");
                cliente [2] = rs.getString("Direccion");
                
                modelo.addRow(cliente);
            }
            
            Tabla.setModel(modelo);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
        
    }
    
